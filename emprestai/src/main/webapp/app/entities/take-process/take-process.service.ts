import axios from 'axios';

import { ITakeProcess } from '@/shared/model/take-process.model';

const baseApiUrl = 'api/take-processes';

export default class TakeProcessService {
  public find(id: number): Promise<ITakeProcess> {
    return new Promise<ITakeProcess>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ITakeProcess): Promise<ITakeProcess> {
    return new Promise<ITakeProcess>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
